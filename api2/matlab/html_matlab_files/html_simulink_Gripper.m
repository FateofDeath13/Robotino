% Robotino can be connected with many number of devices and one of these
% include a gripper. Using the simulink block actions such as
% opening and closing the gripper can be performed.
%% Block properties
%
% *Inputs*
%
% # ComId
% # Enable - set true (1) to enable the gripper, false (0) otherwise.
% # Close - set true (1) to close the gripper, false (0) to open the
% gripper.
%
% *Outputs*
%
% # Opened - is set to true (1) if the gripper is open, false (0)
% otherwise.
% # Closed - is set to true (1) if the gripper is closed, false (0)
% otherwise.
